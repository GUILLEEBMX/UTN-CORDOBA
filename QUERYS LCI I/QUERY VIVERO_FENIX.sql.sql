COD_CONDICION_IVA Int,
DESCRIPCION Varchar(30),
CONSTRAINT PK_CONDICIVA PRIMARY KEY (COD_CONDICION_IVA))
CREATE TABLE FORMAS_PAGO(
COD_FORMA_PAGO Int,
DESCRIPCION Varchar(30),
CONSTRAINT PK_FORMAPAGO PRIMARY KEY (COD_FORMA_PAGO))
CREATE TABLE PLANTAS(
COD_PLANTA Int,
DESCRIPCION Varchar(20),
COD_TIPO_PLANTA Int,
PRECIO Money,
STOCK Int,
CONSTRAINT PK_PLANTAS PRIMARY KEY (COD_PLANTA),
CONSTRAINT FK_PLA_TPLA FOREIGN KEY (COD_TIPO_PLANTA) REFERENCES TIPOS_PLANTAS(COD_TIPO_PLANTA))
